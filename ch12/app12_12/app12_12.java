// app12_12, 數學函數的使用
public class app12_12
{
   public static void main(String args[])
   {
      System.out.println("ceil(3.9)= "+Math.ceil(3.9));
      System.out.println("sin(PI/2)= "+Math.sin(Math.PI/2));
      System.out.println("max(8,2)= "+Math.max(8,2));
   }
}

/* app12_12 OUTPUT---
ceil(3.9)= 4.0
sin(PI/2)= 1.0
max(8,2)= 8
-------------------*/